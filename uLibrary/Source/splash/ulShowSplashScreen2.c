const int ul_neoflashlogo_size = 42534;

#define LARG 256
#define HAUT 192
#define TILE 8
float ul_neoflashlogo_positions[HAUT/TILE][LARG/TILE];

void ul_neoflashlogo_LogoDrawTiles(UL_IMAGE *img)
{
	int x, y;

	ulSetTexture(img);
	ulVertexBegin(GL_QUADS);
	
	for (y=0;y<HAUT/TILE;y++)			{
		for (x=0;x<LARG/TILE;x++)			{
		   ulVertexUVXY(x * TILE, y * TILE, x * TILE, ul_neoflashlogo_positions[y][x]);
		   ulVertexUVXY(x * TILE, y * TILE + TILE, x * TILE, ul_neoflashlogo_positions[y][x] + TILE);
		   ulVertexUVXY(x * TILE + TILE, y * TILE + TILE, x * TILE + TILE, ul_neoflashlogo_positions[y][x] + TILE);
		   ulVertexUVXY(x * TILE + TILE, y * TILE, x * TILE + TILE, ul_neoflashlogo_positions[y][x]);
		}
	}
	
	ulVertexEnd();
	ulVertexHandleDepth();
}

int ulShowSplashScreen2()		{
	UL_IMAGE *logo;
	float y, vy;
	int frameNb, phase;
	float vyList[LARG/TILE];
	float speeds[HAUT/TILE][LARG/TILE];
	int i, j, fade;
   int oldSource = ulGetLoadUtilitySource();
   ulSetLoadUtilitySource(VF_MEMORY);

	ulInitText();
	logo = ulLoadImageFileJPG((const char*)ul_neoflashlogo, ul_neoflashlogo_size, UL_IN_VRAM, UL_PF_5550);
	if (!logo)
		return 0;	

	y = -logo->sizeY;
	vy = 1.0f;
	frameNb = 0;
	phase = 1;
	fade = 0;
	for (i=0;i<LARG/TILE;i++)
		vyList[i] = (rand() % 7500) / 10000.f + 0.25f;
	for (j=0;j<HAUT/TILE;j++)
		for (i=0;i<LARG/TILE;i++)		{
			speeds[j][i] = vyList[i] - (HAUT/TILE - j) * ((rand() % 1000) / 10000.0f + 0.04f);
			ul_neoflashlogo_positions[j][i] = j * TILE;
		}

	//Ensure the second screen is black
	REG_BLDCNT_SUB = BLEND_FADE_BLACK | BLEND_SRC_BG0 | BLEND_SRC_BG1 | BLEND_SRC_BG2 | BLEND_SRC_BG3;
	REG_BLDY_SUB = 0x1f;

	while(fade < 31 && frameNb < 220)		{
		ulReadKeys(0);
		if (phase < 9)		{
			y += vy;
			if (y + logo->sizeY >= HAUT)		{
				if (vy > 0)
					vy -= 0.8f;
				else
					vy -= 0.4f;
				if (phase % 2 == 1)
					phase++;
			}
			else if (phase == 1)
				vy += 0.4f;
			else		{
				if (vy > 0)
					vy += 0.4f;
				else
					vy += 0.8f;
				if (phase % 2 == 0)
					phase++;
			}
		}
		else		{
			y = 0;
			frameNb++;
		}
		if (frameNb > 120)		{
			for (j=0;j<HAUT/TILE;j++)
				for (i=0;i<LARG/TILE;i++)		{
					speeds[j][i] += 0.15f;
					if (speeds[j][i] > 0.0f)
						ul_neoflashlogo_positions[j][i] += speeds[j][i];
				}
		}
		if (fade > 0)
			fade++;
		if ((ul_keys.pressed.value & (UL_KEY_A|UL_KEY_B|UL_KEY_START|UL_KEY_TOUCH)) && fade == 0)
			fade = 1;

		ulStartDrawing2D();
		if (frameNb <= 120)		{
//				logo->stretchY = logo->sizeY * min(stretch, 1);
//				logo->y = y;
			logo->y = y;
			if (y < 0)		{
				logo->stretchY = logo->sizeY;
				logo->stretchX = logo->sizeX;
				logo->x = 0;
			}
			else	{
				logo->stretchY = logo->sizeY - y;
				logo->stretchX = logo->sizeX + y;
				logo->x = - y / 2;
			}

			ulDrawImage(logo);
		}
		else
			ul_neoflashlogo_LogoDrawTiles(logo);

		if (fade > 0)		{
			ulSetAlpha(UL_FX_ALPHA, fade, 3);
			ulDrawFillRect(0, 0, 256, 192, 0);
		}
		ulEndDrawing();
		ulSyncFrame();
	}
	
	ulDeleteImage(logo);

	//Restore the old source
   ulSetLoadUtilitySource(oldSource);

	//Restore second screen
	REG_BLDCNT_SUB = 0;
	REG_BLDY_SUB = 0;
   return 1;
}


